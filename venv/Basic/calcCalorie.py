# 칼로리 계산기
"""
    kitkat : 190 칼로리
    oreos : 502 칼로리
    pringles : 292 칼로리
    twix : 135.9 칼로리
    cheetos : 485 칼로리

    과자를 다양하게 조합해서 먹었을 때 총 몇 칼로리인지 계산
"""
kitkat = 190
oreos = 502
pringles = 292
twix = 135.9
cheetos = 485

# 다양한 과자 조합
print(kitkat + oreos * 2)
print(cheetos * 4)
print(pringles + oreos + twix)
print(pringles * 3 + oreos * 2)
